    {
        $user = User::find($user_id)->get();
        if(!$user){
            return response()->json([
                    'status' => 400,
                    'error' => 'Error'
                ]);
        }else{
            return response()->json([
                        'User' => $user,
                        'status' => 200,
                        'message' => 'success'
                ]);
        }
    }

